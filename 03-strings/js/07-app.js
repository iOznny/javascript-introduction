// Strings with Methods -> toUpperCase(), tolowerCase()

const example07 = 'Monitor 20 inches';

// Convert string to uppercase
console.log('Uppercase -> ' + example07.toUpperCase());
console.log('Uppercase -> ' + 'hola'.toUpperCase());

// Convert string to lowercase
console.log('Lowercase -> ' + example07.toLowerCase());
console.log('Lowercase -> ' + 'A@B.C'.toLowerCase());

// Convert number to string
