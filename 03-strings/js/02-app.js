// Methods and Functions
const tweet = 'Monitor 20 inches';
console.log(tweet);

// property length:  Know the length of the text string
console.log('length' + tweet.length);

// Method indexOf: El método indexOf() retorna el primer índice en el que se puede encontrar 
// un elemento dado en el array, ó retorna -1 si el elemento no esta presente.
console.log('indexOf' + tweet.indexOf('20'));

// Method Includes
console.log('includes' + tweet.includes('Monitor'));
