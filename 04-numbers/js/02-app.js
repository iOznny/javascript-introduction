// Operations with numbers

const operation1 = 30;
const operation2 = 20;

let result;

// Sum
result = operation1 + operation2;
console.log('Sum -> ' + result);

// Subtraction
result = operation1 - operation2;
console.log('Subtraction -> ' + result);

// Multiplication
result = operation1 * operation2;
console.log('Multiplication -> ' + result);

// Division
result = operation1 / operation2;
console.log('Division -> ' + result);

// Module
result = operation1 % operation2;
console.log('Module -> ' + result);