// Increment and Decrement

let result05 = 5;

// Assign and then sum or Sum and then assign 
result05++; // 6
++result05; // 7
console.log('Increment -> ' + result05);

// Assign and then subtract or  Subtract  and then assign 
result05-- // 6
--result05; // 5
console.log('Decrement -> ' + result05);


// Sum and assignment
let sumAssign = 5;
sumAssign += 5;
console.log('Sum and assignment -> ' + sumAssign);

// Subtract and assignment
sumAssign -= 5;
console.log('Subtract and assignment -> ' + sumAssign);
